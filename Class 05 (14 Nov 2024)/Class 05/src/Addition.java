public class Addition {

    public static int addKoro (int a){
        return a+a;
    }

    public static int addKoro (int a, int b){
        return a+b;
    }

    public int addKoro (int a, int b, int c){
        return a+b+c;
    }

    public int addKoro (int a, int b, int c, int d){
        return a+b+c+d;
    }
}
